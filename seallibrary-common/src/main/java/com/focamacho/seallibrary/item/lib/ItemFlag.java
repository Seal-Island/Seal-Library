package com.focamacho.seallibrary.item.lib;

@SuppressWarnings("unused")
public enum ItemFlag {

    /**
     * Mostrar ou esconder encantamentos.
     */
    HIDE_ENCHANTS,

    /**
     * Mostrar ou esconder atributos.
     */
    HIDE_ATTRIBUTES,

    /**
     * Mostrar ou esconder se o item é inquebrável.
     */
    HIDE_UNBREAKABLE,

    /**
     * Mostrar ou esconder o que o item pode quebrar/destruir.
     */
    HIDE_DESTROYS,
    /**
     * Mostrar ou esconder onde o item pode ser construido/colocado
     */
    HIDE_PLACED_ON,

    /**
     * Mostrar ou esconder os efeitos de poção do item
     */
    HIDE_POTION_EFFECTS

}
